CREATE DATABASE SCHOOLE;
USE SCHOOLE ;

CREATE TABLE TEACHERS(
TEACHERSID INT NOT NULL,
TEACHERSNAME VARCHAR(50),
PRIMARY KEY(TEACHERSID)
);

CREATE TABLE  TEACHERS_DT(
PHONENUM VARCHAR(50),
TEACHERSID INT unique,
EMAIL VARCHAR(50),
TEACHERSCOURSES VARCHAR(50),
FOREIGN KEY (TEACHERSID) REFERENCES TEACHERS(TEACHERSID)
);

CREATE TABLE COURSES (
COURSESID INT PRIMARY KEY,
COURSESNAME VARCHAR (50),
STUDENTSID int
);

CREATE TABLE STUDENTS (
STUDENTSNAME VARCHAR (50),
STUDENTSID INT PRIMARY KEY,
STUDENTSPHONENUM INT,
COURSESID int
);


create table coursesStudents (
STUDENTSID int,
COURSESID int,
FOREIGN KEY (STUDENTSID) REFERENCES STUDENTS(STUDENTSID),
FOREIGN KEY (COURSESID) REFERENCES COURSES(COURSESID)
);
